# Funciones
# abs() retornar el valor absoluto
print('Valor absoluto')
a = -23
print(abs(a))

# bin() obtiene la conversion a binario
print('Conversion a binario')
b = 9
print(bin(b))

# chr() retorna el caracter que corresponda al codigo ASCII
print('Valor Unicode ')
c = 97
print(chr(c))

# divmod() retornar el cociente y el resto de una division
print('Cociente y Resto')
d , e =21,3
f , g = divmod(d,e)
print(f,g)

# float() convierte una cadena de texto en un n decimal
print('Conversion a numero decimal')
h = '23.2'
print(float(h))

# hex() convierte a hexadecimal
print('Conversion a hexadecimal')
i = 124
print(hex(i))

# input() obtiene una cadena de texto
print('Funcion Input')
j = input('Ingrese algo:')

# int() convierte una cadena de texto a numero entero
print('Convierte a numero entero')
k = '1234'
print(int(k))

# len() retornar la longitud
print('Retorna la longitud')
l= 1,2,3,4,5,6
print(len(l))

# max() retornar el mayor entre los parametros
print('calcula el mayor')
m,n= 12,1
mayor = max(m,n)
print(mayor)

# min() retornar el menor entre los parametros
print('calcula el menor')
o,p= 12,1
menor = min(o,p)
print(menor)

# oct () obtener la conversion a octal
print('Base octal')
q = 123
print(oct(q))

# ord() retornar el entero unicode
print('Entero unicode')
r = '@'
print(ord(r))

# pow() retorna la potencia
print('Potencia')
s,t = 2,3
print(pow(s,t))

# round() sirve para redondear a la cant de decimales quiera
print('Redondear')
u = 3.2122324
print(round(u,2))

# str() convierte a string (cadena de texto)
v = 23
print(str(v))


